#!/usr/bin/env python3
"""
Validate and inspect the synthetic incident scenarios fixture.

The fixture (datasets/synthetic_scenarios.json) is generated by a Claude agent
reading the runbooks and postmortems from datasets/notion_mock/. Run this script
to verify the fixture is present and well-formed.

To regenerate the fixture, ask Claude Code:
    "Regenerate datasets/synthetic_scenarios.json from the notion_mock runbooks and postmortems"

Usage:
    python datasets/generate_synthetic_scenarios.py
"""
import json
import sys
from pathlib import Path

OUTPUT = Path("datasets/synthetic_scenarios.json")

POSTMORTEM_SERVICE_MAP = {
    "inc-2024-0112": "checkout-payments",
    "inc-2024-0287": "checkout-payments",
    "inc-2025-0044": "checkout-payments",
    "inc-2024-0331": "product-catalog",
    "inc-2025-0071": "product-catalog",
    "inc-2024-0089": "cdn-storefront",
    "inc-2024-0198": "cdn-storefront",
    "inc-2024-0445": "cdn-storefront",
    "inc-2025-0019": "cdn-storefront",
    "inc-2024-0156": "auth-sessions",
    "inc-2024-0302": "auth-sessions",
    "inc-2025-0033": "auth-sessions",
    "inc-2024-0118": "queue-workers",
    "inc-2024-0377": "queue-workers",
    "inc-2025-0058": "queue-workers",
    "inc-2024-0203": "database-cache",
    "inc-2024-0419": "database-cache",
    "inc-2025-0012": "database-cache",
}


def main() -> None:
    if not OUTPUT.exists():
        print(f"Fixture not found: {OUTPUT}", file=sys.stderr)
        print(
            "Regenerate by asking Claude Code:\n"
            '  "Regenerate datasets/synthetic_scenarios.json from the notion_mock runbooks and postmortems"',
            file=sys.stderr,
        )
        sys.exit(1)

    scenarios = json.loads(OUTPUT.read_text())
    print(f"{len(scenarios)} scenarios in {OUTPUT}\n")

    services: dict[str, int] = {}
    for s in scenarios:
        svc = s["service"]
        services[svc] = services.get(svc, 0) + 1
        platforms = [a["platform"] for a in s["alerts"]]
        print(f"  {s['scenario_id']} ({svc}) — {len(s['alerts'])} alerts: {platforms}")

    print()
    print("Services:", dict(sorted(services.items())))


if __name__ == "__main__":
    main()
